var cartLsData = JSON.parse(localStorage.getItem("ecom-cart")) || [];
var mens_items = document.querySelector("#mens_items");
var filter = document.querySelector("#brand");
// var filter = document.querySelector("#price");
function display(data)
{
    mens_items.innerHTML = null;
    data.forEach(function (element)
    {
        var card = document.createElement("div");
        var img = document.createElement("img");
        img.src = element.img;
        var title = document.createElement("p");
        title.textContent = element.brand;
        var desc = document.createElement("h3");
        desc.textContent = element.name;
        var cat = document.createElement("h3");
        cat.textContent = element.category;
        var price  = document.createElement("h2");
        price.textContent = element.price;
        var save = document.createElement("p");
        save.textContent = element.save;
        save.style.color = "red";    
        var button = document.createElement("button");
        button.innerText = "add to cart";
        card.append(img,title,desc,cat,price,save,button);
        button.addEventListener("click",function()
        {
            addToCart(element);
        })
       mens_items.append(card);
    });
}

display(products_data);

filter.addEventListener("change",function()
{
    if(filter.value == "none")
    {
        display(products_data);
    }
    else{
        var filtered = products_data.filter(function (element)
        {
            return element.brand == filter.value;
        })
        display(filtered);
    }
})


function addToCart(element)
{
    cartLsData.push(element);
    localStorage.setItem("ecom-cart",JSON.stringify(cartLsData));
    alert("Product Added Successfully In The Cart");
}
